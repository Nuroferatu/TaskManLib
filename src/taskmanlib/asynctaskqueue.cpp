// ---------------------------------------------------------------------------
// AsyncTaskQueue
// ---------------------------------------------------------------------------
AsyncTaskQueue::AsyncTaskQueue() {
}

AsyncTaskQueue::~AsyncTaskQueue() {
}

// ---------------------------------------------------------------------------
// getTask
// ---------------------------------------------------------------------------
int AsyncTaskQueue::get( void ) {
    if (taskQueue.size() == 0 )
        return 0;
    int v = taskQueue.front();
    taskQueue.pop_front();
    return v;
}

// ---------------------------------------------------------------------------
// put
// ---------------------------------------------------------------------------
void AsyncTaskQueue::put( int i ) {
    taskQueue.push_back( i );
}

